import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "premium" | "interactive" | "glass"
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variantClasses = {
    default: "bg-card text-card-foreground border border-border shadow-soft",
    premium: "card-premium bg-gradient-to-br from-card to-muted/20 border-border/50",
    interactive: "card-interactive bg-card text-card-foreground border border-border hover:border-primary/30 hover:shadow-medium cursor-pointer",
    glass: "glass dark:glass-dark backdrop-blur-sm bg-white/10 dark:bg-black/10 border-white/20 dark:border-white/10"
  }
  
  return (
    <div
      ref={ref}
      className={cn(
        "rounded-xl",
        variantClasses[variant],
        className
      )}
      {...props}
    />
  )
})
Card.displayName = "Card"

